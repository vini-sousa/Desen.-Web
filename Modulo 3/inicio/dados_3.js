// A função 'typeof' é usada pra identificar o tipo da váriavel

// Tipo Número
let idade = 31;
console.log(typeof 31);
console.log(typeof idade);

let salario = 4578.32;
console.log(typeof salario);

// Tipo Boolean
let estaChovendo = true // True ou False (Verdadeiro ou Falso)
console.log(typeof estaChovendo);

// Tipo String
console.log(typeof "Aspas Duplas");
console.log(typeof 'Aspas Simples');